# Replace a string with another string
# s.replace(oldstring, newstring)
# inside s , every occurrence of oldstring is replaced with newstring and the result is returned as a new string

s = ""
s1 = s.replace("difficult", "easy")
print(s1)
# All occurences are replaced
s = "aaaaaaaaaaaaabbbbbbbbb"
s1 = s.replace("a", "b")
print(s1)